<h4>Discussion:</h4>

<p>Rainbows are commonly seen in Iowa after the passage of a rain shower 
or thunderstorm.  We often see afternoon showers that pass from west to east
allowing them to interact with the setting sun in the western sky.  The result
is a colorful display in the eastern sky.  While it is possible to see a 
rainbow during the morning hours, the combination of the sun's position and
clouds rarely produce a scenario for a morning rainbow in Iowa.</p>

<p>The video shows a little known phenomena of how a rainbow is actually
moving from the viewer's perspective.  The movie speeds up time enough
for us to visually notice it is moving.  The reason it is moving is due to
the change in the sun's <a href="http://en.wikipedia.org/wiki/Azimuth">azimuth</a> as the sun sets in the western sky.  If you were to trace a path of
the sun in the sky on a given day, it would not make a vertically straight line
into the horizon.  Think about how a sun dial works with its shadow moving
to indicate the passage of time.  The same concept applies to seeing the
rainbow move.</p>


