<h4>Discussion:</h4>

<p>The video shows a rainbow that appears to rise above the horizon.  The
motion is hard to notice when watching a rainbow without the help of speeding
up time as in this movie.  A previous module, discussed the 
<a href="learn.phtml?v=1">left to right</a> movement of a rainbow and the
same material applies in this case.  The vertical movement is a result of
a change in solar 
<a href="http://en.wikipedia.org/wiki/Zenith">zenith angle</a>, 
which is just a fancy term for how high in the sky off the horizon 
the sun appears.  So as the sun sets in the west, the angle changes and the
rainbow will appear higher in the sky.

